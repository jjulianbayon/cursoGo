package main

import "fmt"

func main() {
	//Ejemplos tratamiento Valores

	fmt.Println("Hola mundo")
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	fmt.Println("go" + "lang")
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	//Ejemplos con Variables
	var a int = 1
	fmt.Println(a)
	var b string = "inicial"
	fmt.Println(b)
	c := 8
	fmt.Println(c)
	var d int
	fmt.Println(d)
	var e = true
	fmt.Println(e)

}
